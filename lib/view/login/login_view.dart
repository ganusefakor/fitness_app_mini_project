        setState(() {
          isLoading = true;
        });
        final result = await authMethods.signUser(
            context: context,
            email: emailnamecontroller.text.trim(),
            password: passwordController.text.trim());
        if (!result) {
             setState(() {
          isLoading = false;
        });
          return;
        } else {
             setState(() {
          isLoading = false;
        });
          // ignore: use_build_context_synchronously
           Navigator.push(
                          context,
                          MaterialPageRoute(
                              builder: (context) => const MainTabView(),
                                  ));
        }
          setState(() {
          isLoading = false;
        });
      } catch (e) {
         setState(() {
          isLoading = false;
        });
        debugPrint(e.toString());
      }
    } else {
       
